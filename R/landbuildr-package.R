#' landbuildr: generate, modify and measure artificial landscapes
#'
#' The landbuildr package provides standardised geo-operators for an evidence based
#' approach to generate and modify spatial patterns. This is centred around
#' punch-card like algorithms that make pipelines of spatial operations
#' transparent and reproducible, just like Jacquard looms made the production of
#' fabric with patterns available at industrial scale.
#'
#' The geo-operators are grouped into three conceptually different classes of
#' functions that \code{generate} spatial patterns into gridded objects (e.g.
#' rasters), \code{modify} gridded objects and \code{measure} gridded objects
#' with a modular set of generic and specific (landscape) metrics.
#'
#' @author \strong{Maintainer, Author}: Steffen Ehrmann
#'   \email{steffen.ehrmann@posteo.de}
#'
#' @seealso \itemize{ \item Github project:
#'   \href{https://github.com/EhrmannS/landbuildr}{https://github.com/EhrmannS/landbuildr}
#'   \item Report bugs:
#'   \href{https://github.com/EhrmannS/landbuildr/issues}{https://github.com/EhrmannS/landbuildr/issues}
#'   }
#' @docType package
#' @name landbuildr
